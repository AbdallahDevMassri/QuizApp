public class Opt {
    int id ;
    String opt;
    
    public Opt(int id, String opt) {
        this.id = id;
        this.opt = opt;
    }
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getOpt() {
        return opt;
    }
    public void setOpt(String opt) {
        this.opt = opt;
    }
}

